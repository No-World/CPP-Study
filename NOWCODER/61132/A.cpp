inline void Solution()
{
    ll k;
    scanf("%lld", &k);
    ll a = k % MOD, b = (k + 1) % MOD, c = (k + 2) % MOD;
    if (a % 2 == 0)
    {
        a >>= 1;
    }
    else if (b % 2 == 0)
    {
        b >>= 1;
    }
    else if (c % 2 == 0)
    {
        c >>= 1;
    }
    if (a % 3 == 0)
    {
        a /= 3;
    }
    else if (b % 3 == 0)
    {
        b /= 3;
    }
    else if (c % 3 == 0)
    {
        c /= 3;
    }
    printf("%lld\n", a * b % MOD * c % MOD);
}

