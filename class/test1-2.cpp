#include <iostream>   //引用输入输出的标准库
using namespace std;  //声明标识符的可见范围
int main()            //声明主函数返回类型
{
    cout<<"* \n";     //输出字符串，换行
    cout<<"** \n";    //输出字符串，换行
    cout<<"*** \n";   //输出字符串，换行
    cout<<"**** \n";  //输出字符串，换行
    cout<<"***** \n"; //输出字符串，换行
    system("pause");  //暂停系统状态
    return 0;         //正常退出，终止程序
}