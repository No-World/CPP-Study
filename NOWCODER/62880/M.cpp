vector<vector<int>> a(N);

inline void Solution()
{
    int n, m;
    scanf("%d%d", &n, &m);
    while (m--)
    {
        int x, y;
        scanf("%d%d", &x, &y);
        a[x].push_back(y);
    }
    for (int i = 1; i <= n; i++)
    {
        int ans = 0;
        for (int j = 0; j < a[i].size(); j++)
        {
            ans += a[a[i][j]].size();
        }
        printf("%d ", ans);
    }
    printf("\n");
}

