inline void Solution()
{
    int n;
    ll ans = 0, sum = 0;
    scanf("%d", &n);
    getchar();
    char s;
    scanf("%c", &s);
    while (s != '\n')
    {
        if (s == '(')
        {
            if (sum > 0)
            {
                ans += sum;
            }
            sum--;
        }
        else
        {
            sum++;
        }
        scanf("%c", &s);
    }
    if (sum)
    {
        printf("-1\n");
    }
    else
    {
        printf("%lld\n", ans);
    }
}

