inline void Solution()
{
    int n;
    db d, h;
    scanf("%d%lf%lf", &n, &d, &h);
    db H[n];
    for (int i = 0; i < n; i++)
    {
        scanf("%lf", &H[i]);
    }
    sort(H, H + n);
    db sum = n;
    for (int i = 1; i < n; i++)
    {
        int x = H[i] - H[i - 1];
        if (x < h)
        {
            sum -= ((x - h) / h) * ((x - h) / h);
        }
    }
    printf("%lf\n", sum * d * h / 2);
}

