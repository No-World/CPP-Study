typedef long long ll;
using namespace std;
const int inf = 0x3f3f3f3f, N = 1e6 + 10;
// const ll INF = __LONG_LONG_MAX__;

ll fa[N], num[N];
map<int, ll> mp;

inline ll find(ll x)
{
    return fa[x] == x ? x : fa[x] = find(fa[x]);
}

inline void merge(ll x, ll y)
{
    x = find(x), y = find(y);
    fa[x] = y;
}

inline void init(ll x)
{
    for (ll i = 1; i <= x; i++)
    {
        fa[i] = i;
    }
}

inline void Solution()
{
    int m, n, cnt = 0;
    ll ans = 0;
    scanf("%d%d", &n, &m);
    init(n);
    for (int i = 0; i < m; i++)
    {
        ll a, b;
        scanf("%lld%lld", &a, &b);
        merge(a, b);
    }
    for (int i = 1; i <= n; i++)
    {
        mp[find(i)]++;
    }
    for (auto x : mp)
    {
        num[cnt] = x.second;
        cnt++;
    }
    sort(num, num + cnt);
    for (int i = 0; i < cnt; i++)
    {
        ans += num[i] * (num[i] - 1) / 2;
    }
    if (ans == m)
    {
        ans += num[0] * num[1];
    }
    printf("%lld\n", ans - m);
}

