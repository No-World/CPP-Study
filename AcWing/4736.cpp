int race[31][N];

inline void Solution(int t)
{
    int n, m, p, ans = 0, pr;
    scanf("%d%d%d", &m, &n, &p);
    for (int i = 0; i < m; i++)
    {
        for (int j = 0; j < n; j++)
        {
            scanf("%d", &race[j][i]);
        }
    }
    for (int i = 0; i < n; i++)
    {
        int pr = race[i][p - 1];
        sort(race[i], race[i] + m);
        ans += race[i][m - 1] - pr;
    }

    printf("Case #%d: %d\n", t + 1, ans);
}

int main(int argc, char const *argv[])
{
    int T = 1;
    // std::ios::sync_with_stdio(false);
    // std::cin.tie(nullptr);
    // std::cout.tie(nullptr);
    // cin >> T;
    scanf("%d", &T);
    for (int i = 0; i < T; i++)
    {
        Solution(i);
    }
    return 0;
}