int a[N];

inline void Solution()
{
    int n, m;
    scanf("%d%d", &n, &m);
    n++;
    for (int i = 1; i < n; i++)
    {
        scanf("%d", &a[i]);
        a[i] += a[i - 1];
    }
    while (m--)
    {
        int l, r;
        scanf("%d%d", &l, &r);
        printf("%d\n", a[r] - a[l - 1]);
    }
}

