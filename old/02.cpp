const int inf = 0x3f3f3f3f, N = 1e6 + 10;
// const ll INF = __LONG_LONG_MAX__;

bool p[N];
int pr[N];
int prn[N];

void Euler()
{
    int cnt = 0;
    p[0] = p[1] = true; // 特例
    for (int i = 2; i < N; ++i)
    {
        if (!p[i])
            pr[cnt++] = i; // 选出素数
        for (int j = 0; j < cnt && pr[j] * i < N; ++j)
        {
            p[pr[j] * i] = true; // 筛出非素数
            if (i % pr[j] == 0)
                break; // 重复筛选,跳出循环
        }
    }
}

inline void Solution()
{
    int n;
    scanf("%d", &n);
    for (int i = 0; i < n; i++)
    {
        int x;
        scanf("%d", &x);
        for (int j = 0; x; j++)
        {
            if (x % pr[j] == 0)
            {
                prn[j]++;
                x /= pr[j];
            }
            x++;
        }
    }
    for (int i = 0; i < N && pr[i]; i++)
    {
        if (prn[i] % n != 0)
        {
            printf("NO\n");
            return;
        }
    }
    printf("YES\n");
    memset(prn, 0, sizeof(prn));
}

int main(int argc, char const *argv[])
{
    Euler();
    int T = 1;
    // std::ios::sync_with_stdio(false);
    // std::cin.tie(nullptr);
    // std::cout.tie(nullptr);
    // cin >> T;
    // scanf("%d", &T);
    while (T--)
    {
        Solution();
    }
    return 0;
}