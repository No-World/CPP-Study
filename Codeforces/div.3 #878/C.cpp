inline int sum(int n)
{
    return n * (n + 1) / 2;
}

inline void Solution()
{
    int n, k, q;
    scanf("%d%d%d", &n, &k, &q);
    long long a[n], cnt = 0, ans = 0;
    for (int i = 0; i < n; i++)
    {
        scanf("%lld", &a[i]);
    }
    for (int i = 0; i < n; i++)
    {
        if (a[i] <= q)
        {
            cnt++;
        }
        else
        {
            cnt = 0;
        }
        if (cnt >= k)
        {
            ans += cnt - k + 1;
        }
    }
    printf("%lld\n", ans);
}

