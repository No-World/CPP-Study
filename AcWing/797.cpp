int a[N], b[N];
inline void Solution()
{
    int n, m;
    scanf("%d%d", &n, &m);
    for (int i = 1; i <= n; i++)
    {
        scanf("%d", &a[i]);
    }
    for (int i = 1; i <= n; i++)
    {
        b[i] = a[i] - a[i - 1];
    }
    while (m--)
    {
        int l, r, c;
        scanf("%d%d%d", &l, &r, &c);
        b[l] += c, b[r + 1] -= c;
    }
    int cnt = 0;
    for (int i = 1; i <= n; i++)
    {
        cnt += b[i];
        printf("%d ", cnt);
    }
    printf("\n");
}

