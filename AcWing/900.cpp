typedef long long ll;
using namespace std;
const int inf = 0x3f3f3f3f, N = 1e3 + 10;
const ll INF = __LONG_LONG_MAX__, MOD = 1e9 + 7;

ll dp[N] = {1};

inline void Solution()
{
    int n;
    scanf("%d", &n);
    for (int i = 1; i <= n; i++)
    {
        for (int j = i; j <= n; j++)
        {
            dp[j] = (dp[j] + dp[j - i]) % MOD;
        }
    }
    printf("%d\n", dp[n]);
}

