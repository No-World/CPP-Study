inline void Solution()
{
    string s1, s2;
    cin >> s1 >> s2;
    int len1 = s1.length(), len2 = s2.length(), ans = 0;
    for (int i = 0; i < len1 - len2 + 1; i++)
    {
        for (int j = 0; j < len2; j++)
        {
            if (s1[i + j] != s2[j])
            {
                break;
            }
            if (j == len2 - 1)
            {
                ans++;
            }
        }
    }
    for (int i = len1 - len2 + 1; i < len1; i++)
    {
        for (int j = 0; j < len2; j++)
        {
            if (i + j < len1)
            {
                if (s1[i + j] != s2[j])
                {
                    break;
                }
            }
            else
            {
                if (s1[i + j - len1] != s2[j])
                {
                    break;
                }
            }
            if (j == len2 - 1)
            {
                ans++;
            }
        }
    }
    printf("%d\n", ans);
}

