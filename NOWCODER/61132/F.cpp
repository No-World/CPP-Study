inline void Solution()
{
    int n, k, y, ans = 0, j;
    scanf("%d", &n);
    for (int i = 1; i <= n; ++i)
    {
        scanf("%d", &a[i]);
        b[a[i]] = i;
    }
    for (int i = 1; i <= n; ++i)
    {
        if (a[i] != i)
        {
            k = a[i];
            swap(a[i], a[b[i]]);
            swap(b[k], b[i]);
            ans++;
        }
    }
    printf("%d\n", ans);
}

