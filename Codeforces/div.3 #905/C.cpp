const int inf = 0x3f3f3f3f, N = 1e5 + 10, mod = 1e9 + 7;
// const ll INF = __LONG_LONG_MAX__, MOD;

int a[N];

inline int check(int n, int k, int num)
{
    int b[N], ans = 0;
    for (int i = 0; i < n; i++)
    {
        b[i] = k - a[i] % k;
        b[i] = (b[i] == k) ? 0 : b[i];
    }
    sort(b, b + n);
    for (int i = 0; i < num; i++)
    {
        ans += b[i];
    }
    return ans;
}

inline void Solution(int T)
{
    int n, k, mn = inf;
    scanf("%d%d", &n, &k);
    for (int i = 0; i < n; i++)
    {
        scanf("%d", &a[i]);
    }
    mn = min(check(n, k, 1), mn);
    if (k == 4)
    {
        mn = min(check(n, 2, 2), mn);
    }
    printf("%d\n", mn);
}

