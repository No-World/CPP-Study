void Solution(int T)
{
    int ans = 0;
    char a;
    for (int i = 1; i < 11; i++)
    {
        int x = i < 6 ? i : 11 - i;
        for (int j = 1; j < 11; j++)
        {
            int y = j < 6 ? j : 11 - j;
            scanf("%c", &a);
            if (a == 'X')
            {
                ans += min(x, y);
            }
        }
        scanf("%c", &a);
    }
    printf("%d\n", ans);
}

