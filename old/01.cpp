typedef long long ll;
using namespace std;
const int inf = 0x3f3f3f3f, N = 1e3 + 10, MOD = 1e9 + 7;
// const ll INF = __LONG_LONG_MAX__;

int a[N], dp[N][N];

inline void Solution()
{
    int n, m;
    scanf("%d%d", &n, &m);
    m = n - m;
    for (int i = 1; i <= n; i++)
    {
        scanf("%d", &a[i]);
        dp[i][1] = 1;
    }
    sort(a + 1, a + n + 1);
    ll res = 0;
    for (int i = 1; i <= n; i++)
    {
        for (int j = 1; j < i; j++)
        {
            if (a[i] % a[j])
            {
                continue;
            }
            for (int k = 2; k <= m; k++)
            {
                dp[i][k] += dp[j][k - 1];
                dp[i][k] %= MOD;
            }
        }
        res = (res + dp[i][m]) % MOD;
    }
    printf("%d\n", res);
}

int main(int argc, char const *argv[])
{
    int T = 1;
    // std::ios::sync_with_stdio(false);
    // std::cin.tie(nullptr);
    // std::cout.tie(nullptr);
    // cin >> T;
    // scanf("%d", &T);
    while (T--)
    {
        Solution();
    }
    return 0;
}