int h[18];

inline void Solution()
{
    int n;
    scanf("%d", &n);
    while (n--)
    {
        int a, b;
        scanf("%d%d", &a, &b);
        if (a == 0)
        {
            int mh = max(h[b], h[b + 1]);
            h[b] = mh + 3, h[b + 1] = mh + 1;
        }
        else if (a == 90)
        {
            int mh = max(max(h[b] + 1, h[b + 1]), h[b + 2]);
            h[b] = mh + 1, h[b + 1] = mh + 1, h[b + 2] = mh + 1;
        }
        else if (a == 180)
        {
            int mh = max(h[b], h[b + 1] + 2);
            h[b] = mh + 1, h[b + 1] = mh + 1;
        }
        else
        {
            int mh = max(max(h[b], h[b + 1]), h[b + 2]);
            h[b] = mh + 1, h[b + 1] = mh + 1, h[b + 2] = mh + 2;
        }
    }
    for (int i = 1; i < 9; i++)
    {
        printf("%d ", h[i]);
    }
    printf("\n");
}

