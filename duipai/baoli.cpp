bool st[N];
int a[N];

inline void Solution(int T)
{
    int n, mx = 0;
    scanf("%d", &n);
    for (int i = 1; i <= n; i++)
    {
        scanf("%d", &a[i]);
        st[a[i]] = 1;
    }
    for (int i = 0; i <= n; i++)
    {
        if (st[i] && i > 0)
        {
            mx = i + 1;
        }
        else if (!st[i])
        {
            break;
        }
    }
    for (int i = 1; i <= n; i++)
    {
        if (!a[i])
        {
            mx = max(mx, max(a[i - 1], a[i + 1]));
        }
    }
    printf("%d\n", mx);
}
