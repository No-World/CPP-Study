inline void Solution()
{
    map<string, bool> softwareData, personData; // Map to store software data

    int n;
    cin >> n;

    for (int i = 0; i < n; ++i)
    {
        string command, software, command1, command2;
        cin >> command;

        if (command == "sudo")
        {
            cin >> command1 >> command2 >> software;
            if (command1 == "pacman")
            {
                softwareData[software] = true; // Installed software
                personData[software] = true;
            }
            else
            {
                cout << "wuwuwu" << endl;
                break; // Exit on "wuwuwu" command
            }
        }
        else if (command == "pacman")
        {
            cin >> command1 >> software;
            if (command1 == "-R")
            {
                softwareData[software] = false; // Uninstalled software
            }
            else if (command1 == "-Rscn")
            {
                softwareData[software] = false; // Uninstalled software and cleared data
                personData[software] = false;
            }
        }
        else if (command == "1")
        {
            cin >> software;
            if (softwareData.find(software) != softwareData.end() && softwareData[software])
            {
                cout << "yes" << endl;
            }
            else
            {
                cout << "no" << endl;
            }
        }
        else if (command == "2")
        {
            cin >> software;
            if (personData.find(software) != personData.end() && personData[software])
            {
                cout << "yes" << endl;
            }
            else
            {
                cout << "no" << endl;
            }
        }
    }
}

int main(int argc, char const *argv[])
{
    std::ios::sync_with_stdio(false);
    std::cin.tie(0);
    std::cout.tie(0);
    int T = 1;
    // scanf("%d", &T);
    while (T--)
    {
        Solution();
    }
    return 0;
}