struct ShuZi
{
    int s;
    char y;
} a[N];

inline bool cmp(ShuZi x, ShuZi y)
{
    if (x.s != y.s)
    {
        return x.s < y.s;
    }
    return x.y < y.y;
}

inline void Solution()
{
    int n;
    scanf("%d", &n);
    for (int i = 0; i < n; i++)
    {
        scanf("%d", &a[i].s);
    }
    getchar();
    for (int i = 0; i < n; i++)
    {
        scanf("%c", &a[i].y);
    }
    sort(a, a + n, cmp);
    ll ans = 0;
    int x = 0, y = 0, z = 0;
    for (int i = 0; i < n;)
    {
        i++;
        x = 1;
        while (a[i].s == a[i - 1].s && a[i].y == a[i - 1].y)
        {
            i++;
            x++;
        }
        if (a[i].s == a[i - 1].s)
        {
            i++;
            y = 1;
            while (a[i].s == a[i - 1].s && a[i].y == a[i - 1].y)
            {
                i++;
                y++;
            }
            ans += x * y;
        }
    }
    printf("%lld\n", ans);
}

