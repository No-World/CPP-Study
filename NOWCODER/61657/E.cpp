int b[N];

inline void Solution()
{
    int n, ans = 0;
    scanf("%d", &n);
    for (int i = 0; i < n; i++)
    {
        int a;
        scanf("%d", &a);
        b[max(0, i - a)]++;
        b[i]--;
        b[i + 1]++;
        b[min(n, a + i + 1)]--;
    }
    for (int i = 0; i < n; i++)
    {
        b[i] += b[i - 1];
        if (b[i])
        {
            ans++;
        }
    }
    printf("%d\n", ans);
}

