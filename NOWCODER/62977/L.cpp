inline void Solution()
{
    int n;
    scanf("%d", &n);

    ll a[n];
    for (int i = 0; i < n; i++)
    {
        scanf("%lld", &a[i]);
    }
    sort(a, a + n);
    for (int i = 0; i < n; i++)
    {
        for (int j = i; j < n; j++)
        {
            if (a[j] == a[j - 1])
            {
                continue;
            }
            else if (a[i] * 10 <= a[j])
            {
                break;
            }
            else if ((a[i] ^ a[j]) == 2 * (a[i] & a[j]))
            {
                printf("%d %d\n", i, j);
                return;
            }
        }
    }
    printf("-1\n");
}

