vector<ll> x;

inline void Solution()
{
    ll k, a, b;
    scanf("%lld%lld%lld", &k, &a, &b);
    if (a > b)
    {
        swap(a, b);
    }
    if ((b % a) == 0)
    {
        printf("%lld\n", a * k);
        return;
    }
    x.push_back(a), x.push_back(b);
    while (--k)
    {
        x.push_back(x[0] + a), x.push_back(x[0] + b);
        x.erase(x.begin());
        sort(x.begin(), x.end());
    }
    printf("%lld\n", x[0]);
}

