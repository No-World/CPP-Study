const int inf = 0x3f3f3f3f, N = 1e3 + 10, mod = 1e9 + 7;
// const ll INF = __LONG_LONG_MAX__, MOD;

void Solution(int T)
{
    int n, mn = inf, ans = 1, a0 = 0;
    scanf("%d", &n);
    while (n--)
    {
        int x;
        scanf("%d", &x);
        if (x == 0)
        {
            a0++;
            continue;
        }
        mn = min(mn, x);
        ans *= x;
    }
    if (a0 == 1)
    {
        printf("%d\n", ans);
    }
    else if (!a0)
    {
        printf("%d\n", ans / mn * (mn + 1));
    }
    else
    {
        printf("0\n");
    }
}

