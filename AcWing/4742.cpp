int h[N], e[M], ne[M], w[N], f[N], idx;

inline void add(int a, int b)
{
    e[idx] = b, ne[idx] = h[a], h[a] = idx++;
}

inline void Solution()
{
    int n;
    scanf("%d", &n);
    for (int i = 1; i <= n; i++)
    {
        scanf("%d", &w[i]);
    }
    memset(h, -1, (n + 1) * 4);
    idx = 0;
    for (int i = 0; i < n; i++)
    {
        int a, b;
        scanf("%d%d", &a, &b);
        add(a, b), add(b, a);
    }
    memset(f, -1, (n + 1) * 4);
    int res = 0;
}

