inline void Solution()
{
    int n;
    scanf("%d", &n);
    ll a[n];
    for (int i = 0; i < n; i++)
    {
        scanf("%lld", &a[i]);
    }
    ll ans = 0, cnt = 0;
    for (int i = 0; i < n; i++)
    {
        if (a[i] < 0)
        {
            cnt++;
            for (int j = i; j < n; j++)
            {
                if (a[j] > 0)
                {
                    i = j;
                    break;
                }
                ans -= a[j];
            }
        }
        if (a[i] < 0)
        {
            break;
        }
        ans += a[i];
    }
    printf("%lld %lld\n", ans, cnt);
}

