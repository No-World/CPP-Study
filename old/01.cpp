int a[N][N], dp[N][N];
inline void Solution(int T)
{
    int x, y, ans = 0;
    scanf("%d%d", &x, &y);
    for (int i = 1; i <= x; i++)
    {
        for (int j = 1; j <= y; j++)
        {
            scanf("%d", &a[i][j]);
        }
    }
    for (int i = 1; i <= x; i++)
    {
        for (int j = 1; j <= y; j++)
        {
            if (a[i][j])
            {
                dp[i][j] = 1 + min({dp[i - 1][j - 1], dp[i - 1][j], dp[i][j - 1]});
            }
            ans = max(ans, dp[i][j]);
        }
    }
    printf("%d\n", ans);
}

