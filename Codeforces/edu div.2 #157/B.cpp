int a[N];

inline void Solution(int T)
{
    int n, ans = 0;
    scanf("%d", &n);
    for (int i = 1; i <= n * 2; i++)
    {
        scanf("%d", &a[i]);
    }
    sort(a + 1, a + 2 * n + 1);
    int x = a[1], y = a[2 * n];
    for (int i = 2; i <= n; i++)
    {
        ans += abs(a[i] - x) + abs(a[2 * n - i + 1] - y);
        x = a[i], y = a[2 * n - i + 1];
    }
    printf("%d\n", ans);
    for (int i = 1; i <= n; i++)
    {
        printf("%d %d\n", a[i], a[2 * n - i + 1]);
    }
}

